    (Int32.logand td4.(byte a 24) 0xff000000l)
    (Int32.logxor
       (Int32.logand td4.(byte b 16) 0x00ff0000l)
       (Int32.logxor
          (Int32.logand td4.(byte c 8) 0x0000ff00l)
          (Int32.logxor
             (Int32.logand td4.(byte d 0) 0x000000ffl)
             rk)))

let te4_subword_rot temp =
  Int32.logxor
    (Int32.logand
       te4.(byte temp 16)
       0xff000000l)
    (Int32.logxor
       (Int32.logand
          te4.(byte temp 8)
          0x00ff0000l)
       (Int32.logxor
          (Int32.logand
             te4.(byte temp 0)
             0x0000ff00l)
          (Int32.logand
             te4.(byte temp 24)
             0x000000ffl)))

let swap4 rk a b =
  for k = 0 to 3 do
    let tmp = rk.(a + k) in
    rk.(a + k) <- rk.(b + k);
    rk.(b + k) <- tmp
  done


let key_setup_enc rk key bits =
  let temp = ref 0l in

  rk.(0) <- get_u32 key 0;
  rk.(1) <- get_u32 key 4;
  rk.(2) <- get_u32 key 8;
  rk.(3) <- get_u32 key 12;

  if bits = 128 then begin
    let i = ref 0 in

  while !i < 10 do begin
    temp := rk.(4 * !i + 3);

    rk.(4 * !i + 4) <-
      Int32.logxor
        rk.(4 * !i)
