       td1.(byte b 16)
       (Int32.logxor
          td2.(byte c 8)
          (Int32.logxor
             td3.(byte d 0)
             rk)))

let final_round_word a b c d rk =
  Int32.logxor
    (Int32.logand te4.(byte a 24) 0xff000000l)
    (Int32.logxor
       (Int32.logand te4.(byte b 16) 0x00ff0000l)
       (Int32.logxor
          (Int32.logand te4.(byte c 8) 0x0000ff00l)
          (Int32.logxor
             (Int32.logand te4.(byte d 0) 0x000000ffl)
             rk)))

