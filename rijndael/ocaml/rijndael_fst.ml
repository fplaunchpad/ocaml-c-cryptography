let key_setup_dec rk key bits =
  let nr = key_setup_enc rk key bits in

  let i = ref 0 in
  let j = ref (4 * nr) in

  while !i < !j do begin
    swap4 rk !i !j;
    i := !i + 4;
    j := !j - 4;
  end done;

for round = 1 to nr - 1 do
  let base = round * 4 in

  rk.(base)     <- inv_mix_key_word rk.(base);
  rk.(base + 1) <- inv_mix_key_word rk.(base + 1);
  rk.(base + 2) <- inv_mix_key_word rk.(base + 2);
  rk.(base + 3) <- inv_mix_key_word rk.(base + 3);
done;
  nr

let encrypt_block rk nr plaintext ciphertext =
  let s0 = ref (Int32.logxor (get_u32 plaintext 0) rk.(0)) in
  let s1 = ref (Int32.logxor (get_u32 plaintext 4) rk.(1)) in
  let s2 = ref (Int32.logxor (get_u32 plaintext 8) rk.(2)) in
  let s3 = ref (Int32.logxor (get_u32 plaintext 12) rk.(3)) in

