let inv_mix_key_word w =
  Int32.logxor
    td0.(Int32.to_int (Int32.logand te4.(byte w 24) 0xffl))
    (Int32.logxor
       td1.(Int32.to_int (Int32.logand te4.(byte w 16) 0xffl))
       (Int32.logxor
          td2.(Int32.to_int (Int32.logand te4.(byte w 8) 0xffl))
