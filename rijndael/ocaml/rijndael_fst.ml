    decr r;

    if !r > 0 then begin
      s0 := round_word !t0 !t1 !t2 !t3 rk.(!rk_pos + 0);
      s1 := round_word !t1 !t2 !t3 !t0 rk.(!rk_pos + 1);
      s2 := round_word !t2 !t3 !t0 !t1 rk.(!rk_pos + 2);
      s3 := round_word !t3 !t0 !t1 !t2 rk.(!rk_pos + 3);
    end
  end done;

