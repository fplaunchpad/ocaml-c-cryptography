  Bytes.set bytes (pos + 1)
    (Char.chr
       (to_int
          (logand
             (shift_right_logical x 16)
             0xffl)));

  Bytes.set bytes (pos + 2)
    (Char.chr
       (to_int
          (logand
             (shift_right_logical x 8)
             0xffl)));

  Bytes.set bytes (pos + 3)
    (Char.chr
       (to_int
          (logand x 0xffl)))

let byte x shift =
  Int32.to_int
    (Int32.logand
       (Int32.shift_right_logical x shift)
       0xffl)

let round_word a b c d rk =
  Int32.logxor
    te0.(byte a 24)
    (Int32.logxor
       te1.(byte b 16)
       (Int32.logxor
          te2.(byte c 8)
          (Int32.logxor
             te3.(byte d 0)
             rk)))

let inv_round_word a b c d rk =
