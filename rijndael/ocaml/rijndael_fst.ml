  let rk_pos = ref 0 in

  while !r > 0 do begin
    t0 := round_word !s0 !s1 !s2 !s3 rk.(!rk_pos + 4);
    t1 := round_word !s1 !s2 !s3 !s0 rk.(!rk_pos + 5);
    t2 := round_word !s2 !s3 !s0 !s1 rk.(!rk_pos + 6);
    t3 := round_word !s3 !s0 !s1 !s2 rk.(!rk_pos + 7);

